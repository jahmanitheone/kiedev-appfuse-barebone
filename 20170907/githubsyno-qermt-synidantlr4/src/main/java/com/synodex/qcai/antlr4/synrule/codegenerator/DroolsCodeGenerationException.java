package com.synodex.qcai.antlr4.synrule.codegenerator;

public class DroolsCodeGenerationException extends Exception {
	private static final long serialVersionUID = -7729059823106890802L;

	public DroolsCodeGenerationException() {
		super();
	}

	public DroolsCodeGenerationException(String message) {
		super(message);
	}

	public DroolsCodeGenerationException(Throwable cause) {
		super(cause);
	}

	public DroolsCodeGenerationException(String message, Throwable cause) {
		super(message, cause);
	}

}
