package com.synodex.qermt.tool.exception;

public class QERMTToolException extends Exception {
	private static final long serialVersionUID = 7624541766917332741L;

	public QERMTToolException() {
		super();
	}

	public QERMTToolException(String message, Throwable cause) {
		super(message, cause);
	}

	public QERMTToolException(String message) {
		super(message);
	}

	public QERMTToolException(Throwable cause) {
		super(cause);
	}

}
